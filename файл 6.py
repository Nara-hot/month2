# Импортируем библиотеку requests
import requests

# Эта библиотека нужна для отправки HTTP-запросов к сайтам из кода на Python.
# Она используется, чтобы получать данные с веб-страниц, работать с API или скачивать файлы.

# Делаем запрос к учебному сайту, чтобы проверить его статус
response = requests.get("https://httpbin.org/get")

# Выводим статус ответа (код 200 означает, что сайт работает успешно)
print("Статус ответа от сайта:", response.status_code)